/**
 * \file
 *      ST NUCLEO Temperature Sensor Resource
 *
 *      This is a simple GET resource that returns the temperature in Celsius
 *      This code has been derived from the original er-rest-example of Contiki
 * \author
 *      STclab team
 */

#include "contiki.h"

#ifdef COMPILE_SENSORS

#include <string.h>
#include "rest-engine.h"
#include "dev/temperature-sensor.h"
#include "dev/sensor-common.h"

static void res_get_handler(void *request, void *response, uint8_t *buffer, uint16_t preferred_size, int32_t *offset);

/* Get Method Example. Returns the reading from temperature sensor. */
RESOURCE(res_temperature,
         "title=\"Temperature\";rt=\"st-temp\"",
         res_get_handler,
         NULL,
         NULL,
         NULL);

static void
res_get_handler(void *request, void *response, uint8_t *buffer, uint16_t preferred_size, int32_t *offset)
{
  int16_t sensor_value = temperature_sensor.value(0);
  unsigned int accept = -1;
  REST.get_header_accept(request, &accept);

  if(accept == -1 || accept == REST.type.TEXT_PLAIN) {
    REST.set_header_content_type(response, REST.type.TEXT_PLAIN);
    snprintf((char *)buffer, REST_MAX_CHUNK_SIZE, "%d.%d", sensor_value/10, ABS_VALUE(sensor_value)%10);

    REST.set_response_payload(response, (uint8_t *)buffer, strlen((char *)buffer));
  } else if(accept == REST.type.APPLICATION_XML) {
    REST.set_header_content_type(response, REST.type.APPLICATION_XML);
    snprintf((char *)buffer, REST_MAX_CHUNK_SIZE, "<Temperature =\"%d.%d\" />", sensor_value/10, ABS_VALUE(sensor_value)%10);

    REST.set_response_payload(response, buffer, strlen((char *)buffer));
  } else if(accept == REST.type.APPLICATION_JSON) {
    REST.set_header_content_type(response, REST.type.APPLICATION_JSON);
    snprintf((char *)buffer, REST_MAX_CHUNK_SIZE, "{'st-temp':{'Temperature':%d.%d}}", sensor_value/10, ABS_VALUE(sensor_value)%10);

    REST.set_response_payload(response, buffer, strlen((char *)buffer));
  } else {
    REST.set_response_status(response, REST.status.NOT_ACCEPTABLE);
    const char *msg = "Supporting content-types text/plain, application/xml, and application/json";
    REST.set_response_payload(response, msg, strlen(msg));
  }
}
#endif /*  COMPILE_SENSORS */
